<p id="view">Exam Hall Seating Management System is an online process developed for colleges to 
simplify examination hall allotment and seating arrangement. It facilitates to 
access the examination information of a particular student in a particular class.<br><br>
The purpose of developing exam hall seating arrangement system is to 
computerized the traditional  way of conducting exams. <br><br>
Another purpose for developing this software is to generate the seating arrangement report 
automatically during exams.The project is developed as a web based application, and it will work 
for a particular institute.
<br><br>
Mostly students are facing many problems for finding the exam hall and their seats respectively.
A newly invented concept can aid for the students for checking their exam halls.
This helps them to identify the floor or get directions to their respective halls without delays.
<br><br>
Student's details have information about all the students who attend the examination.
It contains the name of the Student, Floor Name, Branch of the student and hall number.
<br><br>This project keeps tracks of various details in modules such as Students Details,
Examination timing Details, and Hall Details with proper discription.</p>
